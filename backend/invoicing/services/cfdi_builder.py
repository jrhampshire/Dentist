"""
CFDI 4.0 XML builder service.

Builds a valid CFDI 4.0 XML document from an Invoice model instance,
signs it with the clinic's CSD (Certificado de Sello Digital), and
encodes it as base64 for submission to Finkok.

CFDI 4.0 structure:
- <cfdi:Comprobante> — root element with invoice totals and metadata
  - <cfdi:Emisor> — issuer (clinic) fiscal data
  - <cfdi:Receptor> — receiver (patient) fiscal data
  - <cfdi:Conceptos> — line items
    - <cfdi:Concepto> — individual line item
      - <cfdi:Impuestos> — taxes per concept
        - <cfdi:Traslado> — IVA traslado
  - <cfdi:Impuestos> — global taxes summary
    - <cfdi:Traslados> — IVA total

Sello (seal) is generated by:
1. Creating the original string (cadena original) using XSLT
2. SHA-256 hashing the cadena original
3. Signing the hash with the CSD private key
4. Encoding the signature as base64
"""

import base64
import hashlib
import logging
from datetime import datetime, timezone
from decimal import Decimal
from typing import Any
from xml.etree import ElementTree as ET

from cryptography.hazmat.primitives import hashes, serialization
from cryptography.hazmat.primitives.asymmetric import padding

logger = logging.getLogger(__name__)

# CFDI namespaces
NS_CFDI = "http://www.sat.gob.mx/cfd/4"
NS_XSI = "http://www.w3.org/2001/XMLSchema-instance"
NS_MAP = {
    "cfdi": NS_CFDI,
    "xsi": NS_XSI,
}

# SAT catalog codes
MONEDA_MXN = "MXN"
EXPEDITION_PLACE_DEFAULT = "06300"  # Default CP (override with fiscal_address)


def build_cfdi_xml(invoice: Any, fiscal_config: Any) -> str:
    """
    Build a CFDI 4.0 XML document from an Invoice and FiscalConfig.

    Args:
        invoice: Invoice model instance.
        fiscal_config: FiscalConfig model instance.

    Returns:
        Complete CFDI XML as string (without sello/certificado — PAC adds those).
    """
    # Build the Comprobante element
    comprobante = _build_comprobante(invoice, fiscal_config)

    # Add Emisor
    _add_emisor(comprobante, fiscal_config)

    # Add Receptor
    _add_receptor(comprobante, invoice)

    # Add Conceptos
    _add_conceptos(comprobante, invoice)

    # Add Impuestos (global taxes)
    _add_impuestos(comprobante, invoice)

    # Register namespaces to avoid ns0, ns1 prefixes
    for prefix, uri in NS_MAP.items():
        ET.register_namespace(prefix, uri)

    # Generate XML string
    xml_string = ET.tostring(comprobante, encoding="unicode", xml_declaration=False)

    # Add XML declaration
    xml_with_decl = '<?xml version="1.0" encoding="UTF-8"?>\n' + xml_string

    return xml_with_decl


def sign_cfdi(
    xml_string: str,
    cert_path: str,
    key_path: str,
    key_password: bytes,
) -> str:
    """
    Sign a CFDI XML with the CSD certificate and private key.

    Adds:
    - certificado attribute (base64 of the .cer file)
    - sello attribute (base64 of the RSA-SHA256 signature)
    - noCertificado attribute

    Args:
        xml_string: The CFDI XML string.
        cert_path: Path to the .cer file.
        key_path: Path to the .key file.
        key_password: Decrypted CSD password.

    Returns:
        Signed CFDI XML string.
    """
    # Load certificate
    with open(cert_path, "rb") as f:
        cert_data = f.read()

    cert_b64 = base64.b64encode(cert_data).decode("ascii")

    # Extract certificate serial number
    from cryptography import x509

    cert = x509.load_der_x509_certificate(cert_data)
    cert_serial = _format_cert_serial(cert.serial_number)

    # Load private key
    with open(key_path, "rb") as f:
        key_data = f.read()

    private_key = serialization.load_der_private_key(
        key_data,
        password=key_password,
    )

    # Build cadena original (simplified — in production, use SAT XSLT)
    cadena_original = _build_cadena_original(xml_string, cert_serial)

    # Sign the cadena original
    signature = private_key.sign(
        cadena_original.encode("utf-8"),
        padding.PKCS1v15(),
        hashes.SHA256(),
    )
    sello = base64.b64encode(signature).decode("ascii")

    # Inject certificado and sello into the Comprobante element
    xml_string = xml_string.replace(
        "<cfdi:Comprobante ",
        f'<cfdi:Comprobante certificado="{cert_b64}" sello="{sello}" noCertificado="{cert_serial}" ',
    )

    return xml_string


def encode_for_finkok(xml_string: str) -> str:
    """
    Encode a CFDI XML as base64 for Finkok submission.

    Args:
        xml_string: The CFDI XML string.

    Returns:
        Base64-encoded string.
    """
    return base64.b64encode(xml_string.encode("utf-8")).decode("ascii")


